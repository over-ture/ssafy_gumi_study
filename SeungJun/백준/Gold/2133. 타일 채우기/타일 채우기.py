n = int(input())

'''
1 > 0
2 > 3
3 > 0
4 > 11 = 9 + 2
5 > 0
6 > 41 = 33 + 8 -> 8 = 9-1
...
8 > 153 = 123 + 30 -> 30 = 33-3
10 > 571 = 459 + 112 -> 112 = 123-11
...

-> n = 2k일때만 0 아님
dp[4] = dp[2] * 3 + 2
dp[6] = dp[4] * 3 + dp[2] * 3 - 1 
dp[8] = dp[6] * 3 + dp[4] * 3 - dp[2]
dp[10] = dp[8] * 3 + dp[6] * 3 - dp[4]
'''

dp = [0] * 31
dp[2] = 3
dp[4] = 11
dp[6] = 41

for i in range(4,16):
    dp[2*i] = dp[2*(i-1)] * 3 + dp[2*(i-2)] * 3 - dp[2*(i-3)]

print(dp[n])